function export_results_tex(out, filename)
% export_results_tex.m
% ---------------------------------------------------------
% Exports a compact LaTeX table with mean MSEs and mean regret.
% This is meant as a starting point; you'll likely customize formatting.
% ---------------------------------------------------------

fid = fopen(filename,'w');
if fid<0
    error('export_results_tex:file', 'Cannot open %s', filename);
end

fprintf(fid, '%% Auto-generated by export_results_tex.m\n');
fprintf(fid, '\\begin{tabular}{lcccccc}\\hline\n');
fprintf(fid, 'Scenario & $\\bar e$ & $\\phi$ & $\\rho$ & Policy MSE & Best-arm MSE & Regret \\\\ \\hline\n');

for g=1:numel(out.grid)
    p = out.grid(g).p;
    summ = out.res{g}.summary;

    policy = summ.policy_mse_mean;
    bestarm = min(summ.arm_mse_mean);
    reg = summ.final_regret_mean;

    fprintf(fid, '%s & %.2f & %.2f & %.2f & %.3f & %.3f & %.2f \\\\\n', ...
        p.scenario, p.target_obs_rate, p.phi, p.rho_reveal, policy, bestarm, reg);
end

fprintf(fid, '\\hline\\end{tabular}\n');
fclose(fid);

fprintf('Wrote %s\n', filename);

end
